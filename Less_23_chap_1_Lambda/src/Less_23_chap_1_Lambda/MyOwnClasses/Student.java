package Less_23_chap_1_Lambda.MyOwnClasses;

public class Student {
    public String name;
    public char sex;
    public int age;
    public int course;
    public  double avgGrade;

    public Student(String name, char sex, int age, int course, double avgGrade) {
        this.name = name;
        this.sex = sex;
        this.age = age;
        this.course = course;
        this.avgGrade = avgGrade;
    }
}
